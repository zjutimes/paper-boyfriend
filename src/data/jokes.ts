// 获取角色笑话
export function getJokesByCharacterId(characterId: string): Joke[] {
  switch (characterId) {
    case 'pan-an':
      return panAnJokes;
    case 'song-yu':
      return songYuJokes;
    case 'wei-jie':
      return weiJieJokes;
    case 'lan-ling':
      return lanLingJokes;
    default:
      return panAnJokes;
  }
}

// 获取随机笑话
export function getRandomJoke(characterId: string, excludeIndex?: number): { joke: Joke; index: number } {
  const jokes = getJokesByCharacterId(characterId);
  let index = Math.floor(Math.random() * jokes.length);
  
  // 避免连续重复
  if (excludeIndex !== undefined && jokes.length > 1) {
    while (index === excludeIndex) {
      index = Math.floor(Math.random() * jokes.length);
    }
  }
  
  return { joke: jokes[index], index };
}
